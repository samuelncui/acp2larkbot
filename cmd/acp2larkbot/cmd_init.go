func prompt(scanner *bufio.Scanner, label, defaultVal string) string {
	if defaultVal != "" {
		fmt.Fprintf(os.Stderr, "? %s [%s]: ", label, defaultVal)
	} else {
		fmt.Fprintf(os.Stderr, "? %s: ", label)
	}
	if !scanner.Scan() {
		return defaultVal
	}
	input := strings.TrimSpace(scanner.Text())
	if input == "" {
		return defaultVal
	}
	return input
}
